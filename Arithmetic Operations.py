# For Addition
a = 2
a = a + 2 # Long method
a += 2# Short Method
print(a)

# For subtraction
c = 2
c = c - 1
c -= 0.5
print(c)

# For Multiplication

e = 5
e *= 5
print(e)

# For Division

f = 10
f /= 5
print(f)

# Now Lets remove the decimal value
g = 10
g //= 5
print(g)

#Lets Get a remainder of a value
h = 5
h %= 2
print(h)

# For Square
i = 5
i **= 5
print(i)

